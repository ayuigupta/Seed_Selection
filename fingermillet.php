	<h3>CULTIVATION:</h3>
	<p style="font-size: 17px;">Ragi may be grown as a hot weather crop, from May to September, using long duration varieties and as a cold season crop, from November and December, using early types. Ragi is monocropped in India under irrigation or transplantation. Rainfed it is mostly intercropped with cereals, castor bean, niger, groundnut and pulses. The most common subsidiary crops grown with ragi are fieldbean (Lablab purpureus), pigeon pea (Cajanus cajan), cowpea (Vigna sinensis), and niger (Guizotia abysinnica). With groundnuts, ragi is the subsidiary crop. Liberal manure, mainly sheep and cattle, is applied. Green manures such as cowpeas, sunnhemp, artificial manures and oil cakes, have been used on both irrigated and unirrigated crops. Ragi is chopped and weeded at intervals of 14 days or so. The number and frequency of irrigations varies with seasonal conditions.</p>


	<h3>HARVESTING AND YIELDS:</h3>
	<p style="font-size: 17px;">
		Ragi matures 3-5 months after sowing, depending on variety, season and soil properties. Rainfed crops are cut close to ground, stalks are allowed to wither for a day or two in field, and then bundled and stacked for about 2 months before threshing. To separate the grains, dried earheads are beaten with sticks, sheaves are trodden by bullocks or crushed by stone rollers. The average yield of the rain-fed crop ranges from 10 to 15 q/ha, whereas the irrigated crop yields up to 40 or 50 q/ha. The fodder yield ranges from 30-90 q/ha in the case of the late groups.
