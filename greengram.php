	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Green gram (Phaseolus aureus Roxb) is important pulse crops. The grains (whole or split) are used as a pulse or made into flour, and into straw and husk as fodder for cattle. Grains are also eaten whole (after germinating them), parched, salted, with sugar or boiled with condiments. The important states producing this pulse are Madhya Pradesh, Maharashtra, Uttar Pradesh, Punjab and Andhra Pradesh, Rajasthan, Karnataka and Tamil Nadu.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">The crop needs a well-distributed rainfall. Heavy rains at flowering are harmful, even moist winds at this stage interfere with fertilization. This pulse does best on deep, well-drained loams in the alluvial tract in the north as well as on the red and black soils of peninsular and southern India. It is also cultivated on light or shallow stony soils to clayey soils.</p>


	<h3>VARITIES:</h3>
	<p style="font-size: 17px;">'T 1, 'T 44', 'Pusa Baisakhi', 'PS 7', 'PS 16,' 'S 8','S 9', 'Varsha (H70-16)' Hissar, ' G 65', ' Jawahar 45',' Kopergaon', ' ML 1','Ml 5',' ML 9'</p>

	<h3>SOWING:</h3>
	<p style="font-size: 17px;">
	The kharif crop is sown in June to July, and the rabi crop in September or October. The spring crop is to be sown by 15 February and harvested by the middle of May. The summer crop is sown by 15 April. The seed may be sown by broadcasting or it may be drilled in furrows behind a plough, or with a three-or four-coultered desi drill, in rows 20-30cm apart. The seed-rate varies from 15-20kg per hectare when sown alone and for mixed crop 2-6kg per hectare.
	</p>

	<h3>FERTILIZERS:</h3>
	<p style="font-size: 17px;">
		Manuring with 25-40kg per ha of phosphorus (P2O5) and 25kg per ha of nitrogen (N) should be given at the time sowing. Also seed treatment with biofertilizer viz. Rhizobium at the rate of 25gram per kg of seed is beneficial.
	</p>		

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		Being kharif crop green gram is not required irrigation unless there is dry spell during the kharif season. In summer irrigation should be given according to soil type. Irrigation interval should be 8-10 days in summer. Flowering and pod filling are the critical stages for irrigation.
	</p>	

	<h3>PLANT PROTECTION MEASURES:</h3>
	<p style="font-size: 17px;">
		Pest<br>
1. Aphids<br>
The nymphs and adults suck the sap. The affected leaves turns yellow, get wrinkled and distorted. The insect also exude honeydew on which fungus develops, rapidly covers the plant with sooty mould that interferes with the photosynthetic activity of the plant.<br>
Control<br>
Spraying with 0.05% Endosulfan, 0.02% Phosphamidon, 0.03% Dimethoate, Methyl demeton or Thiometon control the pest effectively.<br><br>

2. Thrips<br>
The adults and nymphs feed on leaves. They scrape the epidermis and such the oozing sap. As a result, light brown patches appear on infested leaves. The affected leaves curl and become dry.<br>
Control<br>Spraying with 0.05% Endosulfan, 0.02% Phosphamidon, 0.03% Dimethoate, Methyl demeton or Thiometon control the pest effectively.<br><br>

3. Pod borer<br>
Caterpillars feed on tender foliage and young pods. They make holes in the pods and feed on developing seeds by inserting anterior half portion of their body inside the pods.<br>
Control<br>
In early stage of attack handpicking of the caterpillars and their destruction. Ploughing fields after the harvest of crop would expose the pupae, which would be destroyed by birds. Spraying the crop with 0.05% Quinalphos or Fenitrohion can successfully control the pest. Spray with HaNPV @ 250 LE/ha.<br><br>
Disease<br>
1. Leaf spot<br>
Angular brown or red spots, with grey or brown centre and reddish-purple border on leave, stalk and pods<br>
Control<br>
Spray with Bordeaux mixture (5:5:50) or 0.2% Ziram.<br><br>

2. Powdery mildew<br>
White powdery patches on leaves and other green parts, later becoming dull coloured and are studded with black dot.<br>
Control<br>
Dust the crop with finely powdered sulphur (200-mesh) @ 20kg/ha.
	</p>

	<h3>HARVESTING AND YIELD:</h3>
	<p style="font-size: 17px;">To avoid loss because of the shattering of pods, the crop is harvested before it is dead ripe. One or two rounds of the picking of pods are also recommended to avoid losses because of the shattering. The plants are uprooted or cut with a sickle, are dried on the threshing-floor for a week or ten days and threshed by beating with sticks, and are winnowed with baskets. The average yield of grain from a pure crop varieties from 5-6q per ha, whereas yields up to 10-15q per ha.
	</p>	

