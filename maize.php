	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Maize in India is an important cereal, and both its area and production have steadily increased during the past two decades. In terms of area and production, it ranks only next to rice, wheat, jowar and . The states of Uttar Pradesh, Bihar, Madhya Pradesh and Rajasthan and Punjab account for over 75 per cent of the area and production of this cereal in the country. Each of the districts of Bahraich, Gonda and Bulandshahr in Uttar Pradesh; Monghyr, saran and Darbhanga in Bihar; Udaipur and Bhilwara in Rajasthan; and Panchmahal in Gujarat put annually over one lakh of hectares in maize. These nine districts account for a quarter of a national area and production of maize in India. Being an important cereal, over 85 per cent of its production in the country is consumed directly as food in various forms; the chapatis are the commonest preparation, whereas roasted ears, popcorn and porridge are other important forms in which maize is consumed. The use of maize in animal feed, particularly for poultry, and in the starch industry is increasing. Green maize plants also furnish a very succulent fodder during spring and monsoon, particularly in northern India.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">Maize does well on a wide range of climatic conditions, and it is grown in the tropical as well as temperate regions, from sea-levels up to altitudes of 2500m. It is however susceptible to frost at all stages of its growth.Maize can be grown successfully in variety of soils ranging from loamy sand to clay loam. However, soils with good organic matter content having high water holding capacity with neutral pH are considered good for higher productivity. Being a sensitive crop to moisture stress particularly excess soil moisture and salinity stresses; it is desirable to avoid low lying fields having poor drainage and also the field having higher salinity. Therefore, the fields having provision of proper drainage should be selected for cultivation of maize.</p>


	<h3>LAND PREPARATION:</h3>
	<p style="font-size: 17px;">A good seedbed for maize should be fine but compact, and free from weeds. It is desirable that the previous crop refuse is buried under with a mouldboard plough. In due course, two or three ploughings with the wooden plough are given. In case the tractor is used, one ploughing followed by a couple of diskings is generally adequate. For kharif cultivation, it is essential that adequate drainage is provided. It may be provided in the form of very shallow surface drains at 40-50 m apart (depending on the slope and the texture of the soil) across the slope and connected to main outlet. Shallow drains do not obstruct the movement of the cattle or tractors during cultivation. Surface drains should be provided during sowing.</p>

	<h3>VARITIES:</h3>
	<p style="font-size: 17px;">Based on the endosperm type, maize varieties may be grouped into dent, flint, floury, waxy, amylose, pop and sweet. In India mostly cream yellow to orange flint early maturing varieties are commonly grown and are put to varied uses, ranging from chapati making, as poor corn, roasted ears, or as green vegetable to starch extraction. Pop corn and sweet varieties are grown only to limited extent. 'Malan White' a dent variety is grown in Kumbalgarh of the Udaipur Division.
	Indigenous maize varieties of northern India show limited variation. With regard to maturity, the maize varieties can be grouped into three categories. Very early maturity types, maturing in 65-75 days, e.g.<br><br>
	1.  Sathi, Kathi and teen Pakhi<br><br>
	2. Medium early maturing varieties subjected to a limited degree of improvement e.g. 'Basi' ,'KT 41','Jaunpur', 'Rudrapur Local', and <br><br>
	3. Late maturing varieties, introductions of contaminated types, e.g.'Malan'. White flint varieties are also grown in certain pockets of Uttar Pradesh and Bihar. Maize varieties cultivated at north eastern high elevations have varied colour mixtures ranging from white, yellow, red to deep purple.</p>

