	<h3>INTERCULTURE:</h3>
	<p style="font-size: 17px;">
		During the initial stage, the growth of the maize plants is suppressed by weeds. Weeding may be done between the rows with bullock or tractors drawn implements, whereas the weeding within the row is done by hand. Two or three weedings may be necessary, following which the crop is earthed up to provide for better standability. No intercultivation after flowering should be done, as it is likely to damage the lateral roots. Effective weed control can be obtained by spraying Simazine or Atrazine @ 1-1.25 spraying, the fields should be free from weeds. For the fodder crop of maize, less of weeding is needed, since the soil surface is nearly covered by a dense population of the maize plants.
	</p>	

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		In regions with about 60 cm of well distributed rainfall during the growing season, any additional irrigation is not necessary. Inadequate soil moisture during flowering and poet flowering particularly during the grain filling period will markedly reduce the yield. During the grain filling period- the most susceptible stage, additional irrigation if needed, should be applied. The total number of irrigations will depend on the rainfall distribution pattern. The spring and rabi crops are entirely raised under irrigation; the number of irrigations may, however, vary from 5-10 depending type of the soil and the prevailing temperatures.
	</p>	

	<h3>PLANT PROTECTION MEASURES:</h3>
	<p style="font-size: 17px;">
		(A) Diseases:<br>
Leaf Blight: Manifestation of oval to round, yellowish-purple spots on leaves. The affected leaves dry up and appear as if burnt. In severe cases, the plants may become stunted, resulting in poorly-formed ears.<br>
Control:
The crop can be sprayed with Dithane M-45 or Indofil @ 35-40 gms or Blue Copper @55 -60 gms in 18 litres water, 2 -3 sprays at 15 days interval, will effectively control the disease.<br><br>

(B) Insect Pests:<br>
1) Stem borer: These borers feed on leaves in the earlier stages. Later on they bore into the stem and cobs, rendering the plant unproductive.<br>
Control:<br>
- After harvest, the stalks and stubbles should be collected from the field and burnt.
- Crop can be sprayed twice with Thiodan 35 EC @ 27 ml in 18 litres water, once 20-25 days after germination and the second spray at the time of grain formation (in endemic areas).<br><br>

2) Red Hairy Caterpillars: Caterpillars feed and destroy the whole plant if the attack is in the early stages of growth.<br>
Control:<br>
- Egg masses and young caterpillars should be collected as soon as detected, and destroyed.
- The field should be ploughed out after the crop is harvested, so as to expose pupae.
- Thiodan 35 EC @ 27 ml in 18 litres water should be sprayed only as last resort.<br><br>

3) Aphids: Tiny, soft bodied insects, usually green in colour. Nymphs and adults suck the sap from leaves and young shoots.<br>
Control:<br>
The crop can be sprayed with Rogor 30 EC @ 18 ml in 18 litres water.<br><br>

4) Grass hoppers: Short-winged hoppers, laying eggs in the soil at a depth of 7.5 to 20 cms, adults feed on foliage.<br>
Control:<br>
Thiodan 35 EC @ 25 ml or Ekalux 25 EC@ 28 mi in 18 litres water can be sprayed.<br><br>

5) Termites: These pests attack young seedlings as well as mature plants; attack is also visible on roots and lower parts of the plants.<br>
Control:<br>
Thiodan 4 % Dust @ 12-15 kg per hectare is applied and mixed well with the soil.
	</p>	
	

	<h3>APPLICATION OF MANNURES AND FERTILIZERS:</h3>
	<p style="font-size: 17px;">
	For obtaining high yield, the maize crop should be heavily manured. Twenty five to thirty cartloads of farmyard manure or compost should be ploughed into the soil before the sowing. For hybrid and composite varieties of maize, 100-120 kg of nitrogen, along with 60 kg of P2O5 and 40 kg of K2O per hectare, is recommended. The precise level of application of phosphorus and potash should be modified in the background of soil analysis. One third of the nitrogen and total quantity of potash and phosphorus should be applied before sowing, while the remaining nitrogen should be applied as a side dressing at the Knee high stage and at tasselling in two equal doses. In freshly leveled fields and soils with very light texture, a soil application of 10-20 kg/ha of zinc sulphate before sowing has also given good results. To control soil infesting insects, 10-20 kg of 10% DDT or BHC per hectare may be mixed with the basic fertilizer application. Farmers planting local varieties may apply 40-60 kg of nitrogen to the soil per hectare.</p>

	<h3>HARVESTING AND STORAGE:</h3>
	<p style="font-size: 17px;">
		The maize crop sown for grain is harvested when the grains are nearly dry and do not contain more than 20 per cent moisture. The appearance of the plant may be misleading, particularly in the case of high yielding hybrids and composites whose grains are dry, while the stalk and leaves may be still green. Ears are removed from the standing crop. Harvested ears are dried in the sun before shelling. In the case of the late-sown crop, farmers prefer to harvest the whole plants and pile them, and the ears are removed are removed later. Maize stalks are used as cattle feed or fuel. In fact, no part of the maize plant, even the cobs from which the grains have been removed, is left unused.<br><br>

Maize grown for fodder should be harvested at the milk to early dough stage; the earlier harvested crop is likely to yield less and have a lower protein content. For silage, however, the late dough is preferred. Both power and hand operated low priced maize shellers are available indigenously. These shellers are considerably more efficient than hand shelling or beating with sticks, the common practice in northern India.<br><br>

Farmers using hybrid maize should not save their own seed for their next crop, as the advanced generation hybrid seeds are likely to yield reduction of 25-30 per cent. However, can save seed from composites and open pollinated varieties, when grown in isolation. At least seeds from 500 to 100 ears of the best yielding and normally spaced plants resistant to prevalent diseases and pests should be bulked. Ears should be dried, shelled and treated with an insecticide and the treated seed is necessary, as the untreated seeds on ears are times badly attacked by stored grain pests and the germination is markedly reduced.<br><br>

Considerable variation in grain yield is observed. The yield levels depend upon the variety, the amount of the fertilizer used, and the rainfall pattern etc. Under irrigated conditions and recommended cultural practices, an average yield of 4 tonnes per hectare in the Indo-Gangetic Plains is not uncommon; in peninsular India and at higher elevations, a mean yield of 5-7 tonnes per hectare has frequently been obtained. Under low fertility and rainfed conditions with poor yielding varieties, a grain yield of about on to two tonnes/ha is obtained.
	</p>	

