	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">It is a tropical to subtropical plant, it prefers fairly to high rainfall areas with high humid conditions. Long day and high temperature condition enhance growth and higher oil production.Ocimum can be grown in rich loam to poor lateritic soils. It can also come up in saline or alkaline soils to moderately acidic soils. Proper drainage is however essential as waterlogging condition results in poor growth.</p>


