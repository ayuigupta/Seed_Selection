	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Clove, the dried unopened flower buds of the evergreen tree, Syzygium aromaticum, (Syn. Eugenia caryophyllus) is an important spices noted for its flavour and medicinal values. It is indigenous to Moluccas island (Indonesia) and was introduced to India around 1800 A.D. by the East Indian Company in their spice garden in Courtallam, Tamil Nadu. The major producers of this spice today are Indonesia, Zanzibar and Madagascar. World production is estimated to be 63,700 tonnes. Indonesia alone accounts for 66 % of the world production. The important clove growing regions in India now are the Nilgiris, Tirunelveli and Kanyakumari districts of Tamil Nadu, Calicut, Kottayam, Quilon and Trivandrum districts of Kerala and South Kanara district of Karnataka.<br>
Food processing industry uses both whole and ground form of cloves in various preparations. Clove oil is used in perfumeries, pharmaceuticals and flavouring industries. Clove oleoresin is also increasingly used in the food processing industry. In Indonesia, the major part is absorbed for making KRETEK cigarette industry. The clove is an evergreen tree often reaching a height of 7 to 15 metres. Leaves posses plenty of oil glands on the lower surface.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">Clove is strictly a tropical plant and requires a warm humid climate having a temperature of 20 to 300C. Humid atmospheric condition and a well distributed annual rainfall of 150 to 250 cm are essential. It thrives well in all situations ranging from sea level upto an altitude of 1500 metres and also in places proximal to and away from sea. Deep black loam soil with high humus content found in the forest region is best suited for clove cultivation. It grows satisfactorily on laterite soils, clay loams and rich black soils having good drainage. Sandy soil is not suitable.</p>

	<h3>PROPAGATION:</h3>
	<p style="font-size: 17px;">Clove is propagated through seed, which is called mother clove. The seeds become available from June to October. Fruits are allowed to ripe on the tree itself and drop down naturally. Such fruit are collected from the ground and sown directly in nursery or soaked in water overnight and the pericarp removed before sowing. They lose their viability within one week after harvest under normal conditions and hence they must be sown immediately after collection from a tree. The second method gives quicker and higher germination. Big sized seeds generally give higher percent of germination.</p>

	<h3>NURSERY PRACTICES:</h3>
	<p style="font-size: 17px;">Beds for sowing seeds are of 15 to 20 cm height, one metre width and a convenient length. The beds should be made of loose soils and over which a layer of sand may be spread (about 5-8cm thick). Seeds are sown at 2 to 3 cm spacing and a depth of about 2cm. The seedbeds have to be protected from direct sunlight. The germination commences in about 10 to 15 days and may last for about 40 days. In higher elevations, germination is delayed considerably, often requires 60 days. The germinated seedlings are transplanted in polythene bags 30cm x 15 cm) containing a mixture of good soil, sand and well decomposed cowdung (in the ratio of about 3:3:1). The seedlings are ready for transplanting in the field when they are 18 to 24 months old. The nurseries are usually shaded and irrigated daily to ensure uniform stand.</p>

	<h3>LAND PREPARATION:</h3>
	<p style="font-size: 17px;">Eastern and northeastern hill slopes, well drained valleys and river banks are ideal for clove. The area selected for raising clove plantations is cleared of scrub growth before monsoon and pits of 60 to 75 cm 3 are dug at a spacing of 6 to 7 meters. The pits are partially filled with topsoil. The seedlings are transplanted in the main field during the beginning of rainy seasons, in June-July, and in low lying areas, towards the end of the monsoon, in September-October. Cloves prefer partial shade.</p>

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		Irrigation is necessary in the initial stages. In places where pronounced drought is normally experienced, pot watering is recommended to save the plants in the initial two or three years. Subsoil irrigation using 20cm length mud tubes or bamboo tubes with the helpful to save the plants during acute summer. Although the trees can survive without irrigation, it is advantageous to irrigate the grown up trees for proper growth and yield.
	</p>	

	<h3>HARVESTING:</h3>
	<p style="font-size: 17px;">
		Clove tree begins to yield from the seventh or eighth year after planting and full bearing stage is attained after about 15 to 20 years. The flowering season is September-October in the plains and December to February at high altitudes. Flower buds are produced on young flush. It takes about 4 to 6 months for the buds to become ready for harvest. At this time, they are less than 2cm long. The optimum stage for picking clove buds is indicated by the change in the colour from green to slightly pinkish tinge. The matured clove buds are carefully picked with hand. Care should be taken to pick the buds at the correct time, as otherwise the quality of the cured produce will be lost to a considerable extent. When the trees are tall and the clove bunches are beyond the reach, platform ladders are used for harvesting. Bending the branches or knocking down the bud clusters with sticks is not desirable as these practices affect the future bearing of the tree.
	</p>	


	<h3>CURING:</h3>
	<p style="font-size: 17px;">The harvested flower buds are separated from the clusters by hand and spread in the drying yard for drying. It takes normally 4 to 5 days for drying. The correct stage of drying is reached when the stem of the bud is dark brown and the rest of the bud lighter brown in colour. Well dried cloves will be only about one-third of the weight of the original. About 11,000 to 15,000 dried cloves make one kilogram.
	</p>


	<h3>YIELD:</h3>
	<p style="font-size: 17px;">Clove often experiences irregular or alternate bearing tendency. A well maintained full grown tree under favorable conditions may give 4 to 8 kg of dried buds. The average annual yields after 15th year may be taken as 2 kg per tree. Clove oil, the spice determining factor, is about 16 to 21 % in the buds. The oil contains 70 to 90 % of free eugenol and 5 to 12 % of eugenyl acetate.
	</p>

