	<h3>LAND PREPARATION:</h3>
	<p style="font-size: 17px;">The wheat crop requires a well-pulverized but compact seed bed for good and uniform germination. Three or four ploughings in the summer, repeated harrowing in the rainy season, followed by three or four cultivations and planking immediately before sowing produce a good, firm seed bed for the dry crop on alluvial soils. For the irrigated crop, the land is given a pre-sowing irrigation (palewa or raund) and the number of ploughings is reduced. Where white ants or other pests are a problem, Aldrin 5% or BHC 10% dust at the rate of 25 kg/ha should be applied to the soil after the last ploughing or before planking.</p>

	<h3>SOWING:</h3>
	<p style="font-size: 17px;">
		a) Sowing time:<br>
	Based on above temperature requirement it has been found that for indigenous wheat last week of October, for long duration dwarf varieties like Kalyansona, Arjun, etc. first fortnight of November and for short duration dwarf wheats like Sonalika, Raj 821 etc. second fortnight is the best sowing time. Under exceptionally late sown condition it may be delayed to latest by 1st week of December beyond which if area is very small transplanting may be practiced.<br><br>

	b) Seed rate:<br>
	Generally, a seed rate of 100 kg/ha has been found to be sufficient for most of the varieties like Kalyan Sona, Arjun, Janak, etc.which have moderate tillering and medium sized grains. But a higher seed rate of 125 kg/ha is desirable for late sown wheat and normal sown for varieties like Sonalika, Raj 821 etc. which have bold grains and shy tillering habits.<br><br>

	c) Spacing: :<br>
	For irrigated, timely sown wheat, a row spacing of 15 to 22.5 cm is followed, but 22.5 cm between the rows is considered to be the optimum spacing. Under irrigated late-sown conditions, a row spacing of 15-18 cm is the optimum. For dwarf wheats, the planting depth should be between 5 and 6 cm. Planting beyond this depth results in a poor stand. In the case of conventional tall varieties, the depth of sowing may be 8 or 9 cm.<br><br>

	d) Seed treatment:<br>
	The seed of loose smut-susceptible varieties should be given solar or hot-water treatment. If the wheat seed is used only for sowing, and not for human consumption or for feeding cattle, it can be treated with Vitavax.
	</p>

	<h3>APPLICATION OF MANNURES AND FERTILIZERS:</h3>
	<p style="font-size: 17px;">
		It is desirable that 2 to 3 tonnes of farmyard manure per hectare or some other organic matter is applied 5 or 6 weeks before sowing. The fertilizer requirement of the irrigated wheat crop are as follows:<br><br>
		a) With assured fertilizer supply:<br>
			Nitrogen (N) @8- - 120 kg/ha<br>
			Phosphorus (P2O5) @ 40- 60 kg/ha<br>
			Potash (K2O) @ 40 kg/ha.<br><br>

		b) Under fertilizer constraints:<br>
			N @ 60-80 kg/ha<br>
			P2O5 @ 30-40 kg/ha<br>
			K2O @ 20-25 kg/ha.<br><br>

			Total quantity of Phosphorus and potash and half the quantity of nitrogen should be applied at the time of sowing. Remaining quantity of Nitrogen should be applied at the time of crown root initiation.For the late sown irrigated wheat crop, the NPK fertilizer dose recommended is:<br>
		N – 60-80 kg/ha<br>
		P2O5 – 30-40 kg/ha<br>
		K2O – 20-25 kg/ha.
	</p>	

	<h3>INTERCULTURE:</h3>
	<p style="font-size: 17px;">
		Generally weeding is done after 1 ½ to 2 months after sowing or weedicides like 2,4 D, Avadex or Nitrofen (Tok E-25) for controling Chenopodium sp, Angallis sp. Asphodelus sp. Phalaris sp. of weeds.
	</p>	

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		The high yielding wheat varieties should be given five to six irrigations at their critical growth stages viz. Crown root initiation, tillering, jointing, flowering, milk and dough which come at 21-25 days after sowing (DAS), 45-60 DAS, 60-70 DAS, 90-95 DAS, 100-105 DAS and 120-125 DAS respectively. Off these irrigation at CRI stage is most important.
	</p>	

