	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Bajra popularly known as Pearl millet, cattail millet or bulrush belongs to the family Graminea. The crop is cultivated for grain as well as for fodder in the arid region of Africa and Asia and as a pasture in U.S.A. It is originated in India or Africa. It is grown all over India except Assam and part of northeast India.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">The crop has a wide adaptability as it may grow under different day lengths, temperature and moisture stress. Most of the varieties developed in India are photosensitive which helps in growing the crop during monsoon, rabi and arid season. It requires low annual rainfall ranging between 40-50 cm and dry weather. The crop may tolerate drought but cannot withstand high rainfall of 90 cm or above. Light soils of low inherent fertility good drainage, mild salinity are best type for this crop. Crop does not tolerate soil acidity</p>


	<h3>LAND PREPARATION:</h3>
	<p style="font-size: 17px;">The crop needs very fine tilt because the seeds are too small. 2-3 harrowings and a ploughing is followed so that a fine tilth may be obtained to facilitate the sowing and proper distribution of seed at appropriate depth.</p>

	<h3>VARITIES:</h3>
	<p style="font-size: 17px;">
NBH-149, VBH-4 developed for Andhra Pradesh, Madhya Pradesh, Gujarat, Maharashtra are capable of producing 14% higher yield.<br>
ICM4-155 gave higher yield than the standard check and adopted for all growing tracts of India.Also H-306, NH-338 and hybrid like MP-204, MP205 have been identified.</p>

	<h3>SOWING:</h3>
	<p style="font-size: 17px;">
		Sowing time :- Most appropriate time of sowing is middle or last week of July<br>
		Seed rate and Spacing:-<br>
		4-5 kg/ha for drilling method<br>
		2.5-3 kg/ha for dibbling method<br>
		spacing 40 –45 cm between rows, 10 –15 cm within rows.<br>
		Seed treatment- The organo-mercurial compound Ceresan, Agrosan should be used @ 2.5-3 kg/ha to control seed borne diseases<br><br>

		Generally the crop requires low quantity of nutrients. But All India Co-ordinated Millet Improvement Project has proved that new plant types of bajra especially hybrids respond to very high doses of fertilizers.<br><br>

		Under rainfed areas application of organic manures such as FYM or compost helps in increasing the crop yield at the rate of 150-200 quintals/ha 80 –100 kg N:40-50 kgP:40-50kgK is recommended dose for hybrid variety.<br><br>

		Fertilizers are applied in split doses, half of nitrogen, full phosphorus and potash should be basal placed at the time of sowing . The organic manures must be applied 20 days before the sowing of the seeds for full decomposition. One fourth dose of nitrogen should be applied about 30 days and 60 days after sowing.
	</p>

	<h3>INTERCULTURE:</h3>
	<p style="font-size: 17px;">
		Thinning or gap filling is followed at the time of first interculture. Hand weeding is followed to control the weeds or application of Atrazine @ 0.5 kg/ha would take care of most of the weeds.
	</p>	

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		Bajra is grown rainfed and crop being drought resistant hardly needs any irrigation, however it is observed that the yield may be significantly increased by irrigating the crop at critical growth stages like maximum tillering, flowering and grain filling stage. Therefore light irrigations and efficient drainage is very essential for bajra production.
	</p>	

	<h3>PLANT PROTECTION MEASURES:</h3>
	<p style="font-size: 17px;">
		a) Insect pests:<br>
Stem bores and grasshoppers are serious pests of bajra controlled by two sprayings with 2 litres of Eldrin 20 c.c and grasshoppers may be controlled by dusting the crop with BHC 5 percent.<br><br>

b) Diseases:<br>
Downy mildew- for controlling this disease seed treatment with fungicide like Dithane Z-78 or M-45 @ 2.0kg/ha in 800-1000 lit. of water.<br>
Smut- Treatment with Ceresan or Thirum @ 1-2 g/kg seeds is effective.<br>
Ergot- Seed treatment with 20% common salt solution followed by washing with fresh water and then treating with Ceresan or Thirum @ 1-2 g/kg seeds is effective.
	</p>	
	

	<h3>HARVESTING AND STORAGE:</h3>
	<p style="font-size: 17px;">a) Harvesting:<br>
		The crop is harvested when grains become hard enough and contain moisture. Two methods are adopted for harvesting the crop Cutting earhead<br>
i) from standing crop followed by cutting of remaining plants later<br>
ii) Cutting of entire plants by sticks and stalking the plants for five days in sun for obtaining grains. Grains are separated either by beating the earheads with sticks or by trampling the earheads under bullock feet.<br><br>

		b) Yield:<br>
		Irrigated crop yields 30-35 quintals/ha, while unirrigated crop yield 12-15 quintals/ha<br><br>

		c) Storage:<br>
		The separated grains must be cleaned and dried in sun to bring about 12-14% moisture after which the grains may be bagged and stored in a moisture proof store.
	</p>	

