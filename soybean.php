	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">Soyabean needs about 15 to 320C temperature for germination but for rapid growth the crop needs higher temperature. The crop requires about 60-65 cm annual rainfall drought at flowering or just before flowering results in flower and pod drops, while rains during maturity impairs the grain quality of soyabean. harmful. The best soil type is sandy loam having good organic matter content.</p>

	<h3>VARITIES:</h3>
	<p style="font-size: 17px;">Monetta, M.A.C.S.-13, M.A.C.S.-57, M.A.C.S.-58, M.A.C.S-124, P.K. 472, J.S.-80-21, J.S. 335</p>

	<h3>SOWING:</h3>
	<p style="font-size: 17px;">
		Two cropping seasons of soybean Kharif and spring.<br>
In case of Kharif season most common time of sowing is onset of monsoon or last week of June to first week of Jully while spring sowing is done between 15th of February and 15th of March.<br><br>

Method of sowing - Line sowing by seed drill followed as it needs less seeds/ha, weeding and hoeing may be done conveniently.<br><br>

Seed treatment- Seeds are treated with rhizobium culture.<br><br>

Spacing- A 45-60 cm X 2.5 cm spacing is good for Kharif crop and 30-45 X 2.5 cm during spring season.<br><br>

Seedling depth- 2-3 cm in heavy soils and 3-4 cm light soils.<br><br>

Seed rate- Soyabean grown for grain purpose needs about 20-30 kg seed/ha but for fodder crop needs about 70-75 kg/ha during Kharif season and 100-120 kg/ha during spring.
	</p>

	<h3>FERTILIZERS:</h3>
	<p style="font-size: 17px;">Soyabean gives higher positive response to applied nitrogen over symbiotically fixed atmospheric nitrogen through its roots alone which proves that nitrogen fixed by soyabean roots is not enough for its and development. Crop is supplied with 10-15% of total nitrogen requirement.<br>
	Application of 25-30 C.L.FYM at the time of sowing proved better results.50 Kg N + 100 Kg P2O5, 20 kg sulphur per ha. Also 25 Kg Zinc sulphate and 10 Kg Borax should be applied.</p>


	<h3>WATER REQUIREMENT:</h3>
	<p style="font-size: 17px;">
		In case of Kharif crop irrigation is not needed and it is grown rainfed. However during summer the crop can be grown only under assured irrigation and it needs about 5-6 irrigations. The crop should be irrigated at the following critical growth stages to minimize water.<br>
1. Sprouting stage<br>
2. Flowering pod initiation and bean filling stages are important from yield point of view
	</p>	

	<h3>CULTIVATION PRACTICES:</h3>
	<p style="font-size: 17px;">
		At the time of sowing one deep ploughing two harrowings should be given tomaintain optimum moisture at sowing.
	</p>	

	<h3>AFTER CARE:</h3>
	<p style="font-size: 17px;">
		Keep plot weed free upto 40 days by one or two hoeings, two weedings upto 40 days. Herbicides such as Toke 25, 1.5 to 2 Kg/ha pre-emergence before sowing controls the weeds.
	</p>	

	<h3>HARVESTING:</h3>
	<p style="font-size: 17px;">
		Crop harvested at proper stage by usual method, threshing machine.
Signs of maturity- Dropping of leaves, leaves turn yellow, moisture content of seed 15%.
	</p>	

		<h3>YIELD:</h3>
	<p style="font-size: 17px;">-Average yield 25g/ha
