	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Fenugreek, a native of South Eastern Europe and West Asia, is cultivated as a leafy vegetable, condiment and as medicinal plant. The fresh tender leaves and stem are consumed as curried vegetable and the seeds are mainly used as spice for flavoring almost all dishes. It has a high medicinal value as it prevents constipation, removes indigestion, stimulates spleen and liver and is appetizing and diuretic. Botanical name of fenugreek is Trigonella foenum-graecum L. It is exported to Saudi Arabia, Japan, Sri Lanka, Korea and UK. The major states growing fenugreek in India are Rajasthan, Madhya Pradesh, Gujarat, Uttar Pradesh, Maharashtra and Punjab. It is an annual herb reaching a height of about 0.9 m, leaves are light green, pinnately trifoliate, flowers – papilionaceous, fruits – legume, long, narrow, curved, tapering with a slender point and containing small deeply furrowed seeds. There are two species of the genus Trigonella, which are of economic importance viz., T.foenum graecum, the common methi and T.corniculata, the Kasuri methi. These two differ in their growth habit and yield. The latter one is a slow growing type and remains in rosette condition during most of its vegetative growth period.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">It has a wide adaptability and is successfully cultivated both in the tropics as well as temperate regions. It is tolerant to frost and freezing weather. It does well in places receiving moderate or low rainfall areas but not in heavy rainfall area. It can be grown on a wide variety of soils but clayey loam is relatively better. The optimum soil pH should be 6.0 to 7.0 for its better growth and development.</p>

	<h3>VARITIES:</h3>
	<p style="font-size: 17px;">Many improved cultivators are now available for cultivation are C01, Rajendra Kanti, RMt-1 and Lam Sel.1</p>

	<h3>LAND PREPARATION AND SOWING:</h3>
	<p style="font-size: 17px;">
		Land is prepared by ploughing thrice and beds of uniform size are prepared. Broadcasting the seed in the bed and raking the surface to cover the seeds is normally followed. But, line sowing is advocated in rows at 20 to 25 cm apart which facilitates the intercultural operations.
<br>
Sowing in the plains is generally taken up in September to November while in the hills, it is grown from March. Approximately 20 to 25 kg of seed is required for one hectare and the seed takes about 6-8 days to complete its germination
	</p>

	<h3>MANNURES AND FERTILIZERS:</h3>
	<p style="font-size: 17px;">
		Besides 15 tonnes of farm yard manure, a fertilizer dose of 25 kg Nitrogen, 25 kg Phosphorus and 50 kg Potash per ha is recommended. Half of the nitrogen dose and the entire quantity of phosphorus and potash are applied basally and the remaining half Nitrogen is applied 30 days after sowing. To obtain more successful leafy growth, nitrogen should be applied after each cutting.
	</p>		

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">
		First irrigation is given immediately after sowing and subsequent irrigation is applied at 7 to 10 days interval.
	</p>	

	<h3>PLANT PROTECTION:</h3>
	<p style="font-size: 17px;">
	Root rot (Rhizoctonia solani) is a serious disease and can be controlled by drenching carbendazim 0.05% first at the onset of the disease and another after one month</p>

	<h3>HARVESTING AND STORAGE:</h3>
	<p style="font-size: 17px;">
		In about 25 to 30 days, young shoots are nipped off 4 to 5 cm above ground level and subsequent cuttings of leaves may be taken after 15 days. It is advisable to take 1 to 2 cuttings before the crop is allowed for flowering and fruiting. When pods are dried, the plants are pulled out, dried in the sun and seeds are threshed by beating with stick or by rubbing with hands. Seeds are winnowed, cleaned and dried in the sun. They may be stored in gunny bags lined with paper.
		<br>
		A yield of 1200 to 1500 kg of seeds and about 800 to 100 kg of leaves may be obtained per hectare in crops grown for both the purposes.
	</p>	

