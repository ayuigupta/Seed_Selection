	<h3>INTRODUCTION:</h3>
	<p style="font-size: 17px;">Tamarind (Tamarindus indica L) is one of the common fruit trees found grown all over India mostly under rainfed conditions, particularly in Tamil Nadu, Maharashtra, Karnataka, Andhra Pradesh, Madhya Pradesh and Orissa. It is also one of the most popular avenue trees which yield useful fruits and timber besides providing shade. Tamarind is believed to be native of Tropical Africa but now cultivated throughout South East Asia, Australia, America.</p>

	<h3>CLIMATE AND SOIL:</h3>
	<p style="font-size: 17px;">Tamarind tree has a acid climatic adaptability and can be grown in humid to dry hot regions. It is very sensitive to frost. The optimum rainfall requirement is 750-1900mm, but can thrive in region with low annual rainfall of 500-750mm. It can thrive on variety of soils, but deep loamy or alluvial soils providing optimum condition for development of its long tap roots are ideal. It can tolerate slightly saline and alkaline soils.</p>


	<h3>VARIETIES:</h3>
	<p style="font-size: 17px;">There are very few well recognized tamarind varieties. However, in the recent past few seedling selections have been identified on the basis of fruit quality and yield. These are: Prathisthan from Fruit Research station, Aurangabad, Tree No 38 located at college of Agriculture, Pune, (Maharashtra) A high yielding type PKM- 1 has been developed by Coimbtore Agril. University (Tamil Nadu)</p>

	<h3>PROPAGATION:</h3>
	<p style="font-size: 17px;">
		Common method of propagation in vogue is by seed. However, being heterozygtes, it does not breed true to type through seed. The prolonged juvenile phase is also due to predominance of seed propogation. Seedlings Trees about 15-20 years for economic yields. Vegetatively propagated plants start yielding after 3-4 years. Vegetative methods such as approach grafting, air layering and budding have been successful. However, layers are suitable for planting under dryland conditions for lack of deep growing tap root system. In order to obtain true to type plants with reduced pre-bearing age, uniform growth and yield vegetative methods like budding and grafting be used in establishing Tamarind orchards.
	</p>

	<h3>PLANTING:</h3>
	<p style="font-size: 17px;">
		In order to provide space and proper medium for development of vigourous and deep tap root system planting should be done in pits of 1 x 1 x 1m size. The pits should be dug during summer and filled in with 2 kg single superphosphate at the base and with a mixture of well decomposed Farm yard manure and top soil. About 100g 10 per cent carbanyl powder be mixed in the soil mixture to avoid the problem of termites. The planting distance may vary from 8 X 8 to 10 X 10m depending on the soil type, closer spacing in shallow soils and wider spacing in deep soils be adopted Rootstock seedlings be planted in situ during monsoon (June-July) and budded by patched method using a bud from improved variety.
	</p>	

	<h3>INTERCULTURE:</h3>
	<p style="font-size: 17px;">Interculture operations like weeding, hoeing be carried out regularly. Cover crops like cowpea, horse gram etc, may be grown during rainy season to control weeds, check soil erosion and improve soil health. Inter crops like drumstick or low growing vegetables can be grown during initial 4-5 years to get early returns.
	</p>	

	<h3>IRRIGATION:</h3>
	<p style="font-size: 17px;">Tamarind is a dry land (rainfed) crop and hence it is normally not irrigated. But young orchards needs to be irrigated, especially during dry spells and summer months. One year old plants be given 10 litres water at an interval of 6-8 days during summer which may be doubled during 2nd and 3rd year. There after irrigation may not be necessary. However if it is provided growth will be faster and better
	</p>	

	<h3>PLANT PROTECTION MEASURES:</h3>
	<p style="font-size: 17px;">There are hardly any pests and diseases on Tamarind plant. So far no serious pests and diseases have been reported. Nor any systematic studies have been undertaken on this plant as it grows mostly under semiwild conditions. The plant has developed natural resistance against this maladies and hence requires hardly any plant protection measures.</p>

	<h3>HARVESTING AND YIELD:</h3>
	<p style="font-size: 17px;">Seed propagated plants start bearing 7-8 years after planting. While grafted or budded plants will start fruiting 4-5 years of planting. Fruits are harvested during January-April months. The yield varies with soil type, climate, propagation method and management practices. A well managed tree yield 300-500 kg ripe pods.
	</p>	

